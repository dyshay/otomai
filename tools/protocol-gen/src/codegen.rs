// ─── Enum generation ─────────────────────────────────────────────

fn generate_enums(enums: &[ProtocolEnum], dir: &Path) -> anyhow::Result<()> {
    let mut mod_rs = String::new();
    writeln!(mod_rs, "// Auto-generated by protocol-gen — do not edit\n")?;

    for e in enums {
        let file_name = to_snake_case(&e.name);
        let file_path = dir.join(format!("{file_name}.rs"));

        let rust_type = match e.value_type.as_str() {
            "int" => "i32",
            _ => "u32", // uint and default
        };

        let mut code = String::new();
        writeln!(code, "// Auto-generated by protocol-gen — do not edit")?;
        writeln!(code, "#![allow(dead_code)]\n")?;
        for (name, val) in &e.values {
            writeln!(code, "pub const {}: {} = {};", name, rust_type, val)?;
        }

        std::fs::write(&file_path, &code)?;
        writeln!(mod_rs, "pub mod {file_name};")?;
    }

    std::fs::write(dir.join("mod.rs"), &mod_rs)?;
    Ok(())
}

// ─── Polymorphic variant enum generation ─────────────────────────

fn generate_variant_enums(hierarchies: &[TypeHierarchy], types_dir: &Path) -> anyhow::Result<()> {
    // Append to types/mod.rs
    let mod_rs_path = types_dir.join("mod.rs");
    let mut mod_rs = std::fs::read_to_string(&mod_rs_path).unwrap_or_default();

    writeln!(mod_rs, "\n// Polymorphic variant enums")?;
    writeln!(mod_rs, "mod variants;")?;
    writeln!(mod_rs, "pub use variants::*;")?;

    std::fs::write(&mod_rs_path, &mod_rs)?;

    // Generate variants.rs with all variant enums
    let mut code = String::new();
    writeln!(code, "// Auto-generated by protocol-gen — do not edit")?;
    writeln!(code, "#![allow(unused_imports, dead_code)]\n")?;
    writeln!(code, "use dofus_io::{{BigEndianReader, BigEndianWriter, DofusDeserialize, DofusSerialize}};")?;
    writeln!(code, "use super::*;")?;
    writeln!(code, "use anyhow::Result;\n")?;

    for h in hierarchies {
        let enum_name = format!("{}Variant", h.base_name);

        // Enum definition
        writeln!(code, "/// Polymorphic enum for {} hierarchy", h.base_name)?;
        writeln!(code, "#[derive(Debug, Clone)]")?;
        writeln!(code, "pub enum {} {{", enum_name)?;
        for (variant_name, _) in &h.variants {
            writeln!(code, "    {}({}),", variant_name, variant_name)?;
        }
        writeln!(code, "}}\n")?;

        // Default impl
        if let Some((first, _)) = h.variants.first() {
            writeln!(code, "impl Default for {} {{", enum_name)?;
            writeln!(code, "    fn default() -> Self {{")?;
            writeln!(code, "        Self::{}({}::default())", first, first)?;
            writeln!(code, "    }}")?;
            writeln!(code, "}}\n")?;
        }

        // get_type_id
        writeln!(code, "impl {} {{", enum_name)?;
        writeln!(code, "    pub fn get_type_id(&self) -> u16 {{")?;
        writeln!(code, "        match self {{")?;
        for (variant_name, id) in &h.variants {
            writeln!(code, "            Self::{}(_) => {},", variant_name, id)?;
        }
        writeln!(code, "        }}")?;
        writeln!(code, "    }}\n")?;

        // deserialize_with_id
        writeln!(code, "    pub fn deserialize_with_id(type_id: u16, reader: &mut BigEndianReader) -> Result<Self> {{")?;
        writeln!(code, "        match type_id {{")?;
        for (variant_name, id) in &h.variants {
            writeln!(code, "            {} => Ok(Self::{}({}::deserialize(reader)?)),", id, variant_name, variant_name)?;
        }
        writeln!(code, "            _ => Err(anyhow::anyhow!(\"Unknown {} type_id: {{}}\", type_id)),", h.base_name)?;
        writeln!(code, "        }}")?;
        writeln!(code, "    }}")?;
        writeln!(code, "}}\n")?;

        // DofusSerialize
        writeln!(code, "impl DofusSerialize for {} {{", enum_name)?;
        writeln!(code, "    fn serialize(&self, writer: &mut BigEndianWriter) {{")?;
        writeln!(code, "        match self {{")?;
        for (variant_name, _) in &h.variants {
            writeln!(code, "            Self::{}(v) => v.serialize(writer),", variant_name)?;
        }
        writeln!(code, "        }}")?;
        writeln!(code, "    }}")?;
        writeln!(code, "}}\n")?;

        // DofusDeserialize (delegates to base type, user should use deserialize_with_id)
        writeln!(code, "impl DofusDeserialize for {} {{", enum_name)?;
        writeln!(code, "    fn deserialize(reader: &mut BigEndianReader) -> Result<Self> {{")?;
        if let Some((first, _)) = h.variants.first() {
            writeln!(code, "        Ok(Self::{}({}::deserialize(reader)?))", first, first)?;
        } else {
            writeln!(code, "        Err(anyhow::anyhow!(\"Cannot deserialize {} without type_id\"))", enum_name)?;
        }
        writeln!(code, "    }}")?;
        writeln!(code, "}}\n")?;
    }

    std::fs::write(types_dir.join("variants.rs"), &code)?;
    Ok(())
}

