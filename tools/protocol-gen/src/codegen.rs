fn to_snake_case(s: &str) -> String {
    let mut result = String::new();
    for (i, ch) in s.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 {
                result.push('_');
            }
            result.push(ch.to_lowercase().next().unwrap());
        } else {
            result.push(ch);
        }
    }
    result
}

fn sanitize_field_name(name: &str) -> String {
    let snake = to_snake_case(name);
    match snake.as_str() {
        "type" => "r#type".to_string(),
        "self" => "self_".to_string(),
        "super" => "super_".to_string(),
        "move" => "move_".to_string(),
        "ref" => "ref_".to_string(),
        "match" => "match_".to_string(),
        "mod" => "mod_".to_string(),
        "box" => "box_".to_string(),
        "yield" => "yield_".to_string(),
        "return" => "return_".to_string(),
        "where" => "where_".to_string(),
        _ => snake,
    }
}

/// Recursively collect all TypeManager base type names from a field type.
fn collect_type_manager_refs(ft: &FieldType, out: &mut HashSet<String>) {
    match ft {
        FieldType::TypeManager(name) if !name.is_empty() => {
            out.insert(name.clone());
        }
        FieldType::Vector { inner, .. } => {
            collect_type_manager_refs(inner, out);
        }
        _ => {}
    }
}
