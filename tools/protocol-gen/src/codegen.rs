fn field_type_to_rust(ft: &FieldType) -> String {
    match ft {
        FieldType::Bool => "bool".into(),
        FieldType::Byte => "u8".into(),
        FieldType::UByte => "u8".into(),
        FieldType::Short => "i16".into(),
        FieldType::UShort => "u16".into(),
        FieldType::Int | FieldType::VarInt => "i32".into(),
        FieldType::UInt | FieldType::VarUInt => "u32".into(),
        FieldType::Long | FieldType::VarLong => "i64".into(),
        FieldType::ULong | FieldType::VarULong => "u64".into(),
        FieldType::Float => "f32".into(),
        FieldType::Double => "f64".into(),
        FieldType::String => "String".into(),
        FieldType::VarShort => "i16".into(),
        FieldType::VarUShort => "u16".into(),
        FieldType::ByteArray => "Vec<u8>".into(),
        FieldType::Type(name) => {
            if name.is_empty() { "Vec<u8>".into() }
            else {
                // Strip Vector.<> wrapper if present
                if let Some(inner) = name.strip_prefix("Vector.<").and_then(|s| s.strip_suffix('>')) {
                    format!("Vec<{inner}>")
                } else {
                    name.clone()
                }
            }
        }
        FieldType::Vector { inner, .. } => {
            let inner_type = field_type_to_rust(inner);
            format!("Vec<{inner_type}>")
        }
        FieldType::TypeManager(name) => {
            if name.is_empty() { "Vec<u8>".into() }
            else {
                let base = name.strip_prefix("Vector.<")
                    .and_then(|s| s.strip_suffix('>'))
                    .unwrap_or(name);
                format!("Box<{}Variant>", base)
            }
        }
    }
}

